const SERIES_BASES = {
	6: E6_BASE,
	12: E12_BASE,
	24: E24_BASE,
	48: E48_BASE,
	96: E96_BASE,
	192: E192_BASE,
};

export function generateESeriesValues(eseries: ESeries, startDecade: number, endDecade: number) {
	const decades = endDecade - startDecade;
	let values = new Array<number>(SERIES_BASES[eseries].length * decades);

	let i = 0;
	for (let d = startDecade; d < endDecade; d++) {
		const multiplier = 10 ** d;
		for (const b of SERIES_BASES[eseries]) {
			values[i++] = b * multiplier;
		}
	}

	return values;
}

export function eSeriesBaseFromValue(value: number) {
	const log10 = Math.floor(Math.log10(value));
	const multiplier = 10 ** log10; 
	return value / multiplier;
}

export function isBaseInESeries(base: number, eSeries: ESeries) {
	let eseriesBases: number[];
	switch (eSeries) {
		case 6: eseriesBases = E6_BASE;			break;
		case 12: eseriesBases = E12_BASE;		break;
		case 24: eseriesBases = E24_BASE;		break;
		case 48: eseriesBases = E48_BASE;		break;
		case 96: eseriesBases = E96_BASE;		break;
		case 192: eseriesBases = E192_BASE;		break;
	}

	let lo = 0;
	let hi = eseriesBases.length - 1;

	while (lo <= hi) {
		const mid = (lo + hi) >>> 1;
		const val = eseriesBases[mid];

		if (Math.abs(val - base) < 1e-5) return true;
		else if (val < base) lo = mid + 1;
		else hi = mid - 1;
	}

	return false;
}

export function isValueBaseInEseries(value: number, eSeries: ESeries) {
	return isBaseInESeries(eSeriesBaseFromValue(value), eSeries);
}